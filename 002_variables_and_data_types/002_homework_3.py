# –∞ –Ω–µ —Ñ—É–Ω–∫—Ü–∏—é math.sqrt, —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –∫–æ–º–ø–ª–µ–∫—Å–Ω—ã–µ —á–∏—Å–ª–∞ –≤ —Å–ª—É—á–∞–µ, –µ—Å–ª–∏ –ø–æ–¥–∫–æ—Ä–µ–Ω–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ.

a = float(input('a = '))
