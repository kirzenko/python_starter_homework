# Пройтись по диапазону чисел от 0 до 100, выводить все числа, при этом пропустить число 4 и,
# как только цикл достигнет числа 18 - выйти из цикла.

# while
x = 0
while x <= 100:
    x += 1
    if x == 4:
        continue
    if x == 19:
        break
    print('число равно - ', x)

# for
for i in range(100):
    if i == 4:
        continue
    if i == 19:
        break
    print('Число равно - ', i)